def _highlight_high_fat(row):
    color = "background-color: #ffb3ba" if row["F(g)"] > 20 else ""
    return [color] * len(row)


for cat in CATEGORIES:
    show_nutrition = cat.get("show_nutrition", True)
    with st.expander(cat["title"], expanded=True):
        if show_nutrition:
            df = pd.DataFrame(
                cat["items"],
                columns=["食品名", "目安量", "kcal", "P(g)", "F(g)", "C(g)"],
            )
            if "メイン" in cat["title"]:
                st.dataframe(
                    df.style.apply(_highlight_high_fat, axis=1),
                    hide_index=True,
                    use_container_width=True,
                )
            else:
                st.dataframe(df, hide_index=True, use_container_width=True)
        else:
            names = [item[0] for item in cat["items"]]
            df = pd.DataFrame(names, columns=["食品名"])
            st.dataframe(df, hide_index=True, use_container_width=True)

